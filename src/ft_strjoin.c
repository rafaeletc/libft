#include	"libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	size;

	size = 0;
	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	str = malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	while (*s1 != '\0')
		str[size++] = *s1++;
	while (*s2 != '\0')
		str[size++] = *s2++;
	str[size] = '\0';
	return (str);
}
